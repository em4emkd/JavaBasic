public class Task01 {

	public static void main(String[] args) {

		// create an array and retrieve all elements in reverse order

		int[] arr = { 10, 20, 30, 40, 50, 60 };

		for (int i = arr.length - 1; i >= 0; i--) {
			System.out.print(arr[i]+" ");
		}
		
		
		

	}

}
